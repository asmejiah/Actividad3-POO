    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String nombre;
        int horas;
        double salario_basico, salario_mensual;
        
        nombre=txtNombre.getText();
        horas=Integer.parseInt(txtHoras.getText());
        salario_basico=Double.parseDouble(txtSalario.getText());
       
        salario_mensual=salario_basico*horas;
        
        if(salario_mensual>450000){
            txtRNombre.setText(nombre);
            txtRSalario.setText(String.valueOf(salario_mensual));
        }
        else{
            txtRNombre.setText(nombre);
            txtRSalario.setText("");
        }
        
    }//GEN-LAST:event_btnCalcularActionPerformed

